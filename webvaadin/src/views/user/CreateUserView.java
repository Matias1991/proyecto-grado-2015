	public CreateUserView() {
		
		super("Usuarios", "Crear usuario");
		
		buildMainLayout();
		setCompositionRoot(mainLayout);

		userType.addItems("Socio", "Gerente", "Administrador");
		// cargo por defecto Socio
		userType.select("Socio");

		btnAdd.addClickListener(new Button.ClickListener() {

			@Override
			public void buttonClick(ClickEvent event) {
				btnAdd.setEnabled(false);
				if (validateCreateUser()) {
					// Creo el ValueObjet cargandolo con los datos carados por
					// el usuario
					User newUser = new User();
					newUser.setUserName(txtUserName.getValue());
					newUser.setName(txtName.getValue());
					newUser.setLastName(txtCreateLastName.getValue());
					newUser.setEmail(txtEmail.getValue());
					if (userType.getValue().equals("Socio")) {
						newUser.setUserTypeId(2);
					} else if (userType.getValue().equals("Gerente")) {
						newUser.setUserTypeId(3);
					} else  if (userType.getValue().equals("Administrador")) {
						newUser.setUserTypeId(1);
					}

					if (UserController.createUser(newUser)) {
						new PopupWindow("AVISO", "Usuario creado correctamente");
						// limpio los campos
						cleanInputs();
					}
				}
				btnAdd.setEnabled(true);
			}
		});

		btnCancel.addClickListener(new Button.ClickListener() {

			@Override
			public void buttonClick(ClickEvent event) {
				cleanInputs();
				UI.getCurrent().getNavigator().navigateTo(Constant.View.CATALOGUSERS);				
			}
		});

	}
	
	public void cleanInputs(){
		txtName.setValue("");
		txtEmail.setValue("");
		txtUserName.setValue("");
		txtCreateLastName.setValue("");
		userType.setValue("Socio");
		txtName.setRequiredError(null);
		txtEmail.setRequiredError(null);
		txtUserName.setRequiredError(null);
		txtCreateLastName.setRequiredError(null);
	}

	@Override
	public void enter(ViewChangeEvent event) {
		super.enter(event);
		if (RequestContext.getRequestContext() != null) {
			btnAdd.setEnabled(true);
			txtName.focus();
			cleanInputs();
		}
	}

	private boolean validateCreateUser() {
		boolean validate = true;
		if(!txtName.isValid() || !txtEmail.isValid() || !txtCreateLastName.isValid() || !txtUserName.isValid()){
			txtName.setRequiredError("Es requerido");
			txtEmail.setRequiredError("Es requerido");
			txtCreateLastName.setRequiredError("Es requerido");
			txtUserName.setRequiredError("Es requerido");
			validate = false;
		}		
		return validate;
	}

